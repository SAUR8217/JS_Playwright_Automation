function add(a, b) {
    return a + b;
}

function multiply(a, b) {
    return a * b;
}

const num1 = 5;
const num2 = 3;

const sum = add(num1, num2);
const product = multiply(num1, num2);

console.log(`The sum of ${num1} and ${num2} is: ${sum}`);
console.log(`The product of ${num1} and ${num2} is: ${product}`);
